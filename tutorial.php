                <h3 class="title1" style="margin-top:5%;">Pertama-tama, ketahui dahulu arti dan fungsi dan <b>form pengaduan</b></h3>
				<div class="blank-page widget-shadow scroll" id="style-2 div1">
					<p>
                        <b>Pengaduan</b> adalah penyampaian keluhan yang disampaikan kepada pengelola pengaduan pelayanan pelayanan publik
                        pada instansi pemerintah atas pelayanan pelaksana yang tidak sesuai dengan standar pelayanan, atau pengabaian kewajiban
                        dan/atau pelanggaran larangan oleh pengelola pengaduan pelayanan publik. 
                    </p>
				</div>

                <h3 class="title1" style="margin-top:5%;">Kedua, pastikan hal-hal berikut ini sebelum anda mulai melapor</h3>
				<div class="blank-page widget-shadow scroll" id="style-2 div1">
					<p>
                    1. Laporan anda relevan dengan kinerja pemerintah. <br>
                    2. Menggunakan bahasa Indonesia yang baik dan benar. <br>
                    3. Bukan merupakan ujaran kebencian, SARA, dan caci maki. <br>
                    4. Bukan merupakan laporan yang sudah disampaikan dan masih dalam proses penanganan.
                    </p>
				</div>

                <h3 class="title1" style="margin-top:5%;">Terakhir, perhatikan kolom-kolom yang wajib di isi</h3>
				<div class="blank-page widget-shadow scroll" id="style-2 div1">
					<p>
                    1. <b>Judul Laporan</b> : Merupakan kesimpulan dari suatu permasalahan, inti dari suatu laporan yang disampaikan. <br>
                    2. <b>Isi Laporan</b> : Menceritakan kronologis kejadian yang ingin dikeluhkan. Jika dibutuhkan, sertakan juga data diri Anda
                    berupa nama dan NIK serta keterangan lainnya seperti nomor KIP/BPJS/KKS/PKH/KPS.<br>
                    3. <b>Tanggal Kejadian</b> : Tanggal ketika Anda menerima pelayanan yang kurang memuaskan. <br>
                    4. <b>Lokasi Kejadian</b> : Lokasi Anda menerima pelayanan yang kurang memuaskan (Lebih spesifik anda menginput, lebih baik). <br>
                    5. <b>Instansi Tujuan</b> : Instansi yang berwenang terhadap pengaduan yang diberikan. <br>
                    6. <b>Kategori Laporan</b> : Kategori yang sesuai dengan laporan yang diadukan. <br>
                    7. <b>Foto</b> : Foto yang sesuai dengan laporan yang diadukan (Format JPG, JPEG, PNG, GIF).
                    </p>
