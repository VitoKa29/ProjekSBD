				<h2 class="title1" style="text-align:center;">Apa Itu Ayo Lapor ?</h2>
                <br><br>
				<div class="blank-page widget-shadow scroll" id="style-2 div1">
					<p>
                    Pengelolaan pengaduan pelayanan publik di setiap organisasi penyelenggara di Indonesia belum terkelola 
                    secara efektif dan terintegrasi. Masing-masing organisasi penyelenggara mengelola pengaduan secara parsial 
                    dan tidak terkoordinir dengan baik. Akibatnya terjadi duplikasi penanganan pengaduan, atau bahkan bisa terjadi 
                    suatu pengaduan tidak ditangani oleh satupun organisasi penyelenggara, dengan alasan pengaduan bukan kewenangannya. 
                    Oleh karena itu, untuk mencapai visi dalam good governance maka perlu untuk mengintegrasikan sistem pengelolaan 
                    pengaduan pelayanan publik dalam satu pintu. Tujuannya, masyarakat memiliki satu saluran pengaduan secara Nasional.
                    </p>
                    <p>
                    Untuk itu Pemerintah Republik Indonesia membentuk Sistem Pengelolaan Pengaduan Pelayanan Publik Nasional 
                    (SP4N) - Ayo Lapor adalah layanan penyampaian semua aspirasi 
                    dan pengaduan masyarakat Indonesia melalui beberapa kanal pengaduan yaitu website www.ayolapor.rf.gd, SMS 1705 
                    (Telkomsel, Indosat, Three), Twitter @ayolapor1708 serta aplikasi mobile (Android dan iOS). Lembaga pengelola SP4N-Ayo Lapor
                     adalah Kementerian Pendayagunaan Aparatur Negara dan Reformasi Birokrasi (Kementerian PANRB) sebagai Pembina Pelayanan
                      Publik, Kantor Staf Presiden (KSP) sebagai Pengawas Program Prioritas Nasional dan Ombudsman Republik Indonesia 
                      sebagai Pengawas Pelayanan Publik. Ayo Lapor telah ditetapkan sebagai Sistem Pengelolaan Pengaduan Pelayanan Publik Nasional 
                      (SP4N) berdasarkan Peraturan Presiden Nomor 76 Tahun 2013 dan Peraturan Menteri Pendayagunaan Aparatur Negara dan Reformasi 
                      Birokrasi Nomor 3 Tahun 2015.
                    </p>
                    <p>
                    SP4N-Ayo Lapor dibentuk untuk merealisasikan kebijakan “no wrong door policy” yang menjamin hak masyarakat agar pengaduan 
                    dari manapun dan jenis apapun akan disalurkan kepada penyelenggara pelayanan publik yang berwenang menanganinya. 
                    SP4N bertujuan agar:
                    <br><br>
                    1. Penyelenggara dapat mengelola pengaduan dari masyarakat secara sederhana, cepat, tepat, tuntas, dan terkoordinasi dengan baik <br>
                    2. Penyelenggara memberikan akses untuk partisipasi masyarakat dalam menyampaikan pengaduan<br>
                    3. Meningkatkan kualitas pelayanan publik.
                    </p>
